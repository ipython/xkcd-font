    // ── Cut-and-extend algorithm ───────────────────────────────────────────
    // Takes a glyph outline, shifts every coordinate past a threshold by N
    // font units, and stitches jittered cubic sub-segments across the gap.
    // Cuts are applied in order from the glyph config; Y cuts rotate the
    // coordinate frame so the same X-extend logic handles any lean angle.

    function _segmentize(commands) {
        const segs = [];
        let cx = 0, cy = 0, sx = 0, sy = 0;
        for (const c of commands) {
            const op = c[0];
            if (op === 'M') {
                sx = cx = c[1]; sy = cy = c[2];
                segs.push({ type:'M', from:[cx,cy], to:[cx,cy] });
            } else if (op === 'L') {
                segs.push({ type:'L', from:[cx,cy], to:[c[1],c[2]] });
                cx = c[1]; cy = c[2];
            } else if (op === 'C') {
                segs.push({ type:'C', from:[cx,cy], to:[c[5],c[6]],
                            ctrl:[[c[1],c[2]],[c[3],c[4]]] });
                cx = c[5]; cy = c[6];
            } else if (op === 'Q') {
                segs.push({ type:'Q', from:[cx,cy], to:[c[3],c[4]],
                            ctrl:[[c[1],c[2]]] });
                cx = c[3]; cy = c[4];
            } else if (op === 'Z') {
                segs.push({ type:'L', from:[cx,cy], to:[sx,sy] });
                segs.push({ type:'Z', from:[sx,sy], to:[sx,sy] });
                cx = sx; cy = sy;
            }
        }
        return segs;
    }

    function _bboxOf(segs) {
        let xmin=Infinity, ymin=Infinity, xmax=-Infinity, ymax=-Infinity;
        for (const s of segs) for (const p of [s.from, s.to, ...(s.ctrl||[])]) {
            if (p[0]<xmin) xmin=p[0]; if (p[0]>xmax) xmax=p[0];
            if (p[1]<ymin) ymin=p[1]; if (p[1]>ymax) ymax=p[1];
        }
        return { xmin, ymin, xmax, ymax };
    }

    // Hand-wobble spectrum: ~700- and ~300-unit periods.  Higher frequencies
    // only surface at fine subdivision and read as jitter, not pen wobble.
    function _noiseAt(x, seed) {
        return Math.sin(x * 0.009 + seed)
             + Math.sin(x * 0.021 + seed * 1.7) * 0.45;
    }

    // Catmull-Rom → cubic bezier — gives smooth curves through the jittered
    // anchors rather than a polyline.
    function _anchorsToCubics(anchors) {
        const out = [];
        const n = anchors.length;
        for (let i = 0; i < n - 1; i++) {
            const P0 = anchors[Math.max(0, i - 1)];
            const P1 = anchors[i];
            const P2 = anchors[i + 1];
            const P3 = anchors[Math.min(n - 1, i + 2)];
            const c1 = [P1[0] + (P2[0] - P0[0]) / 6, P1[1] + (P2[1] - P0[1]) / 6];
            const c2 = [P2[0] - (P3[0] - P1[0]) / 6, P2[1] - (P3[1] - P1[1]) / 6];
            out.push({ type:'C', from:P1, to:P2, ctrl:[c1, c2] });
        }
        return out;
    }

    // Apply fn to every coordinate point in a segment list — used to
    // enter and exit the rotated frame for Y-axis extension.
    function _mapSegs(segs, fn) {
        return segs.map(s => {
            const r = { type:s.type, from:fn(s.from), to:fn(s.to) };
            if (s.ctrl) r.ctrl = s.ctrl.map(fn);
            return r;
        });
    }

    function _extend(segs, cut, N, subN, amp, seed) {
        if (!(N > 0)) return segs;
        const shift = x => (x > cut ? x + N : x);
        const gapJitter = x => {
            const t = (x - cut) / N;
            if (t <= 0 || t >= 1) return 0;
            return Math.sin(Math.PI * t) * amp * _noiseAt(x, seed);
        };
        const out = [];
        for (const s of segs) {
            if (s.type === 'M' || s.type === 'Z') {
                out.push({ type:s.type, from:[shift(s.from[0]), s.from[1]],
                                          to:  [shift(s.to[0]),   s.to[1]] });
                continue;
            }
            const crosses = (s.from[0] <= cut && s.to[0] > cut)
                         || (s.from[0] >  cut && s.to[0] <= cut);
            if (!crosses) {
                // Segment lies entirely on one side of the cut: just shift.
                const seg = { type:s.type,
                              from:[shift(s.from[0]), s.from[1]],
                              to:  [shift(s.to[0]),   s.to[1]] };
                if (s.ctrl) seg.ctrl = s.ctrl.map(p => [shift(p[0]), p[1]]);
                out.push(seg);
                continue;
            }
            const goingRight = s.from[0] < s.to[0];
            const xL = goingRight ? s.from[0] : s.to[0];
            const xR = goingRight ? s.to[0]   : s.from[0];
            const yL = goingRight ? s.from[1] : s.to[1];
            const yR = goingRight ? s.to[1]   : s.from[1];
            const tCut = (cut - xL) / (xR - xL);
            const yCut = yL + tCut * (yR - yL);
            const fromP = [shift(s.from[0]), s.from[1]];
            const toP   = [shift(s.to[0]),   s.to[1]];
            const anchors = [fromP];
            if (goingRight) {
                for (let i = 0; i <= subN; i++) {
                    const x = cut + (i / subN) * N;
                    anchors.push([x, yCut + gapJitter(x)]);
                }
            } else {
                for (let i = 0; i <= subN; i++) {
                    const x = cut + N - (i / subN) * N;
                    anchors.push([x, yCut + gapJitter(x)]);
                }
            }
            anchors.push(toP);
            for (const c of _anchorsToCubics(anchors)) out.push(c);
        }
        return out;
    }

    // Apply the cuts listed in g.config.cuts in order, returning the extended
    // segments (font units, baseline-up).  Nx / Ny are the total extensions;
    // they are divided evenly across all x / y cuts respectively.  Each cut's
    // optional `lean` (deg) tilts that cut's extension axis: the frame is
    // rotated so _extend always works along a single axis.  List y cuts from
    // highest pct to lowest so each successive cut's threshold is not shifted
    // by an earlier pass (and likewise rightmost-first for x cuts).
    function buildExtendedSegs(g, Nx, Ny, seed) {
        const cfg = g.config;
        const segs0 = _segmentize(g.commands);
        const bb = g.bbox;
        const w = bb.xmax - bb.xmin, h = bb.ymax - bb.ymin;

        const xCuts = cfg.cuts.filter(c => c.axis === 'x');
        const yCuts = cfg.cuts.filter(c => c.axis === 'y');
        const Nperx = xCuts.length ? Nx / xCuts.length : 0;
        const Npery = yCuts.length ? Ny / yCuts.length : 0;
        const subNx = Nperx > 0 ? Math.max(2, Math.round(Nperx / cfg.unitsPerSeg)) : 0;
        const subNy = Npery > 0 ? Math.max(2, Math.round(Npery / cfg.unitsPerSeg)) : 0;
        const refX = (bb.xmin + bb.xmax) / 2;
        const refY = (bb.ymin + bb.ymax) / 2;

        let xIdx = 0, yIdx = 0, out = segs0;
        for (const cut of cfg.cuts) {
            const isY = cut.axis === 'y';
            const Nper = isY ? Npery : Nperx;
            const subN = isY ? subNy : subNx;
            if (Nper <= 0) { isY ? yIdx++ : xIdx++; continue; }

            // Y cuts start from a -90° base (mapping y→x in the rotated
            // frame); x cuts start from 0°.  `lean` further tilts the cut.
            const phi = ((isY ? -90 : 0) - (cut.lean || 0)) * Math.PI / 180;
            const s = Math.sin(phi), c = Math.cos(phi);
            const rotate = phi !== 0;
            const fwd = p => [c * p[0] - s * p[1], s * p[0] + c * p[1]];
            const inv = p => [c * p[0] + s * p[1], -s * p[0] + c * p[1]];

            // Pick a point on the un-rotated cut line; its rotated x becomes
            // the threshold _extend uses.
            const cutCoord = isY ? bb.ymin + h * (cut.pct / 100)
                                 : bb.xmin + w * (cut.pct / 100);
            const refPt = isY ? [refX, cutCoord] : [cutCoord, refY];
            const thresh = rotate ? fwd(refPt)[0] : cutCoord;
            const seedOff = isY ? (19 + yIdx * 7) : xIdx;

            if (rotate) out = _mapSegs(out, fwd);
            out = _extend(out, thresh, Nper, subN, cfg.amp, seed + seedOff);
            if (rotate) out = _mapSegs(out, inv);

            isY ? yIdx++ : xIdx++;
        }
        return out;
    }

    function _segsToPath(segs) {
        const f = n => +n.toFixed(2);
        const out = [];
        for (const s of segs) {
            if      (s.type === 'M') out.push(`M${f(s.to[0])} ${f(s.to[1])}`);
            else if (s.type === 'L') out.push(`L${f(s.to[0])} ${f(s.to[1])}`);
            else if (s.type === 'C') out.push(`C${f(s.ctrl[0][0])} ${f(s.ctrl[0][1])} ${f(s.ctrl[1][0])} ${f(s.ctrl[1][1])} ${f(s.to[0])} ${f(s.to[1])}`);
            else if (s.type === 'Q') out.push(`Q${f(s.ctrl[0][0])} ${f(s.ctrl[0][1])} ${f(s.to[0])} ${f(s.to[1])}`);
            else if (s.type === 'Z') out.push('Z');
        }
        return out.join(' ');
    }

    // ── Hand-drawn vinculum (font's emdash, X-extended) ────────────────────
    // Sizes the emdash glyph to (pixelWidth × pixelHeight): pick a font-units-
    // per-pixel scale from pixelHeight, compute the X extension needed to
    // reach pixelWidth, then bake the path.  No y stretching — the emdash's
    // natural stroke shape is preserved.
    function vinculumHTML(pixelWidth, pixelHeight) {
        const g = EXTENSIBLE_GLYPHS.emdash;
        const bb = g.bbox;
        const natH = bb.ymax - bb.ymin;
        const natW = bb.xmax - bb.xmin;
        const Sx = pixelHeight / natH;
        const targetW = pixelWidth / Sx;
        const Nx = Math.max(0, targetW - natW);
        const segs = buildExtendedSegs(g, Nx, 0, 5);
        const d = _segsToPath(segs);
        const totalW = natW + Nx;
        // viewBox covers (xmin, -ymax, totalW, natH) so SVG y-down matches
        // the path's y-up coords via a -1 y-scale on the inner <g>.
        return `<svg xmlns="http://www.w3.org/2000/svg" width="${pixelWidth}px" height="${pixelHeight}px" viewBox="${bb.xmin} ${-bb.ymax} ${totalW} ${natH}" style="display:block;overflow:visible">
          <g transform="scale(1,-1)" fill="currentColor" stroke="none">
            <path d="${d}"/>
          </g>
        </svg>`;
    }

    // ── Hand-drawn sqrt surd ───────────────────────────────────────────────
    // Picks the natural radical (U+221A) for short stems and switches to the
    // unencoded `radical.tall` stem-with-shallow-lean once the requested
    // height grows past TALL_SURD_RATIO × the natural radical height —
    // preserving xkcd character at normal size, avoiding the visual
    // collapse at large size.
    const TALL_SURD_RATIO = 1.6;

    // The "short" test keys off the VINCULUM height (the radicand box).
    // surdRect.height is always ≈1.0×fontSize regardless of the radicand,
    // so it can't tell sqrt(π) from sqrt(b²).  Measured ratios:
    // sqrt(π)≈0.77, sqrt(b²)≈0.93, sqrt(b²−4ac)≈0.97 → 0.85 splits the
    // no-ascender case from the superscript cases.
    const SURD_SHORT_THRESHOLD_FRAC = 0.85;

    // Per-glyph parameters.  nudgeFrac is the downward bar offset (fraction
    // of fontSize) from vinculumRect.top — pulls a high-floating bar down
    // to a short radicand, or lifts a thick bar clear of a superscript.
    // leadingPx is the gap between the surd's stem corner and the start of
    // the radicand.
    const SURD_PARAMS = {
        radical: {
            // Short radicands (sqrt(π)) need a big downward nudge to hug
            // the character; tall ones (b² superscript) only need a small
            // one to keep our thicker bar clear of the ascender.
            nudgeFrac: ({isShort}) => isShort ? 0.28 : 0.10,
            leadingPx: ({fontSizePx}) => fontSizePx * 0.16,
        },
        'radical.tall': {
            nudgeFrac: () => 0.10,
            // Taper leading DOWN as the surd grows: a moderately tall
            // radicand (quadratic formula) crowds with too little lead-in,
            // but a very tall one looks over-spaced with a big gap.
            leadingPx: ({fontSizePx, radicandH}) =>
                Math.max(fontSizePx * 0.05,
                         fontSizePx * 0.22 - radicandH * 0.045),
        },
    };

    function _pickSurd(targetH_units_at_naturalRadicalScale) {
        const natH = EXTENSIBLE_GLYPHS.radical.bbox.ymax -
                     EXTENSIBLE_GLYPHS.radical.bbox.ymin;
        return targetH_units_at_naturalRadicalScale > natH * TALL_SURD_RATIO
            ? 'radical.tall' : 'radical';
    }

    // Maximum-y anchor point — for a surd this is the top of the bar near
    // the stem corner.  Must be computed AFTER extension because Y extension
    // shifts everything above the cut upward; the pre-extension max-y point
    // ends up inside the stem and stops being the visible bar top.
    function _surdJunctionFromSegs(segs) {
        let bestX = 0, bestY = -Infinity;
        for (const s of segs) for (const p of [s.from, s.to, ...(s.ctrl || [])]) {
            if (p[1] > bestY) { bestY = p[1]; bestX = p[0]; }
        }
        return { x: bestX, y: bestY };
    }

    // Build the surd SVG given target dimensions in pixels (bar pixel width,
    // total surd pixel height) and a font-units-per-pixel scale Sx.  Returns
    // { html, junctionPx } so the caller can position the overlay so the
    // junction lands at a chosen on-screen point.
    // Caller passes the resolved glyphName and the exact pixel height we
    // want the rendered glyph to be (bar-top to tick-bottom).  No more
    // implicit descender compensation — the caller sizes around the
    // radicand and a target descender directly.
    function buildSurdSvg(barWidthPx, renderedHeightPx, name, Sx) {
        const g    = EXTENSIBLE_GLYPHS[name];
        const bb   = g.bbox;
        const natH = bb.ymax - bb.ymin;
        const targetH_units = renderedHeightPx / Sx;

        // Bar width: the natural bar extends from the source-glyph junction
        // to bb.xmax.  We compute Nx from that span (pre-extension) but the
        // anchor point used for placement is taken from the *extended* segs
        // below — Y extension moves the bar's top upward.
        const natJct = (function () {
            let bx = 0, by = -Infinity;
            for (const c of g.commands) {
                const i = (c[0] === 'C') ? 5 : (c[0] === 'Q') ? 3 :
                          (c[0] === 'M' || c[0] === 'L') ? 1 : -1;
                if (i < 0) continue;
                if (c[i + 1] > by) { by = c[i + 1]; bx = c[i]; }
            }
            return { x: bx, y: by };
        })();
        const naturalBarW = bb.xmax - natJct.x;
        const targetBarW  = barWidthPx / Sx;
        const Nx = Math.max(0, targetBarW - naturalBarW);
        const Ny = Math.max(0, targetH_units - natH);

        const segs = buildExtendedSegs(g, Nx, Ny, 5);
        const d = _segsToPath(segs);
        const eb = _bboxOf(segs);
        const jct = _surdJunctionFromSegs(segs);
        const margin = 20;
        const vbX = eb.xmin - margin;
        const vbY = -(eb.ymax + margin);
        const vbW = (eb.xmax - eb.xmin) + 2 * margin;
        const vbH = (eb.ymax - eb.ymin) + 2 * margin;

        const widthPx  = vbW * Sx;
        const heightPx = vbH * Sx;

        // Junction in SVG pixel coords (relative to the svg's left/top corner).
        const junctionPx = {
            x: (jct.x - vbX) * Sx,
            y: (-jct.y - vbY) * Sx,
        };

        // Returns the SVG markup without positioning styles; caller wraps it
        // in a positioned container after computing where junctionPx should
        // land on screen.
        const svg = `<svg class="xkcd-overlay" xmlns="http://www.w3.org/2000/svg"
            width="${widthPx}px" height="${heightPx}px" viewBox="${vbX} ${vbY} ${vbW} ${vbH}"
            style="display:block;overflow:visible;pointer-events:none;">
          <g transform="scale(1,-1)" fill="currentColor" stroke="none">
            <path d="${d}"/>
          </g>
        </svg>`;
        return { svg, junctionPx, widthPx, heightPx, glyphName: name };
    }

    // ── Replacement passes ──────────────────────────────────────────────────
    // Scale for the surd glyph (px per font unit).  Smaller scale → thinner
    // strokes (since the glyph's stroke thickness in font units scales with
    // Sx) and longer Ny extension is used to reach the requested surd
    // height.  490 produced about a 1.5×fontSize natural radical but the
    // strokes were too thick; 850 gives thinner strokes at the cost of more
    // stem extension on natural-height surds.
    const SURD_NATURAL_SCALE = 850;
    // How far below the radicand's bottom we want the surd's tick to
    // extend (fraction of fontSize).  Picked so the surd visually
    // encloses the radicand with the hand-drawn tick descender.
    const SURD_DESCENDER_FRAC = 0.45;
    // Floor on the rendered surd height (fraction of fontSize) — safety
    // for very small radicands.
    const SURD_MIN_HEIGHT_FRAC = 1.0;
    // Extra bar length past the radicand's right edge for short single-line
    // radicands (fraction of fontSize), so e.g. sqrt(π)'s bar doesn't end
    // abruptly at the glyph.
    const SURD_TRAILING_FRAC = 0.12;
    // Render the bar SVG at this multiple of the CSS border height.  The
    // emdash glyph's bbox is the full visible stroke, so >1× makes the
    // visible bar thicker than the typeset reference.  ~2× matches the
    // density of the previous hand-drawn bar visually.
    const BAR_PIXEL_MULT = 2;

    // The radicand's vinculum (a CSS border-top in MathJax's CHTML) gives
    // us the bar's intended width and left edge.
    function _findVinculum(msqrt) {
        for (const el of msqrt.querySelectorAll('*')) {
            if (el.closest('mjx-surd')) continue;
            const nearestFrac = el.closest('mjx-mfrac');
            if (nearestFrac && msqrt.contains(nearestFrac)) continue;
            const cs = getComputedStyle(el);
            if (cs.borderTopStyle === 'solid' && parseFloat(cs.borderTopWidth) > 0) {
                return el;
            }
        }
        return null;
    }

    // Resolve everything the surd builder needs: which glyph, base Sx,
    // bar width, rendered height, leading gap, and downward nudge.  All
    // size decisions key off the radicand height (the vinculum box) and
    // fontSize.
    function _planSurd({fontSizePx, radicandH, vinculumWidth}) {
        const Sx = fontSizePx / SURD_NATURAL_SCALE;
        const glyphName = _pickSurd(radicandH / Sx);
        const params = SURD_PARAMS[glyphName];
        const isShort = radicandH <= fontSizePx * SURD_SHORT_THRESHOLD_FRAC;

        const leadingPx  = params.leadingPx({fontSizePx, radicandH});
        const trailingPx = isShort ? fontSizePx * SURD_TRAILING_FRAC : 0;
        const barWidthPx = vinculumWidth + leadingPx + trailingPx;
        const nudgePx    = fontSizePx * params.nudgeFrac({isShort});

        // Surd's full rendered height — bar top to tick bottom.  We want
        // the tick to land DESCENDER_FRAC×fontSize below the radicand's
        // bottom for all cases; since the bar is at vincTop + nudge,
        // that means rendered = radicandH + descender − nudge.
        const renderedHeightPx = Math.max(
            radicandH + fontSizePx * SURD_DESCENDER_FRAC - nudgePx,
            fontSizePx * SURD_MIN_HEIGHT_FRAC);

        return {glyphName, Sx, barWidthPx, renderedHeightPx, leadingPx, nudgePx};
    }

    function replaceSqrtSymbols(root) {
        const scope = root || document;
        scope.querySelectorAll('mjx-msqrt').forEach(msqrt => {
            if (msqrt.dataset.sqrtReplaced) return;
            msqrt.dataset.sqrtReplaced = '1';

            const surd = msqrt.querySelector('mjx-surd');
            const vinculumEl = surd && _findVinculum(msqrt);
            if (!surd || !vinculumEl) return;

            const msqrtRect    = msqrt.getBoundingClientRect();
            const vinculumRect = vinculumEl.getBoundingClientRect();
            const fontSizePx   = parseFloat(getComputedStyle(msqrt).fontSize);

            const plan = _planSurd({
                fontSizePx,
                radicandH: vinculumRect.height,
                vinculumWidth: vinculumRect.width,
            });
            const built = buildSurdSvg(plan.barWidthPx, plan.renderedHeightPx,
                                       plan.glyphName, plan.Sx);

            // Anchor the bar-corner junction `leadingPx` to the left of the
            // bar's intended start, and vertically at vinculumRect.top —
            // i.e. where MathJax actually draws the bar.
            const barLeft = vinculumRect.left - msqrtRect.left;
            const barTop  = vinculumRect.top  - msqrtRect.top;
            const svgLeft = barLeft - plan.leadingPx - built.junctionPx.x;
            const svgTop  = barTop  - built.junctionPx.y + plan.nudgePx;

            surd.dataset.xkcdHidden = '1';
            surd.style.visibility = 'hidden';
            vinculumEl.dataset.xkcdHidden = '1';
            vinculumEl.style.borderTopColor = 'transparent';
            if (getComputedStyle(msqrt).position === 'static') {
                msqrt.dataset.xkcdPos = '1';
                msqrt.style.position = 'relative';
            }

            msqrt.insertAdjacentHTML('beforeend',
                `<div class="xkcd-overlay" style="position:absolute;left:${svgLeft}px;top:${svgTop}px;pointer-events:none;">${built.svg}</div>`);
        });
    }

    // ── Vinculums + array rules (border-top/left/right → hand-drawn lines) ─
    //
    // MathJax CHTML expresses three different things as CSS borders:
    //   • fraction bars            — `border-top` on the mjx-mfrac element
    //   • `\hline`                 — `border-top` on every MJX-MTD in a row
    //   • column rules from `|`    — `border-left/right` on every MJX-MTD in a column
    //
    // We replace all of them with the hand-drawn emdash extender.  The naïve
    // per-element approach (draw one segment per cell) was producing visibly
    // segmented vertical lines because each MJX-MTD only spans its own cell
    // height.  Instead we group: cells in the same table that share an edge
    // coordinate become one rule, and we draw a single continuous line
    // spanning the bounding box of the group.  Non-table elements (fraction
    // bars) form their own singleton groups, preserving previous behaviour.
    function _collectRuleGroups(container, side) {
        // Map<groupKey, { side, w, cells: [...] }>.  Group key encodes the
        // enclosing table (so two tables in one container don't merge) plus
        // the edge's rounded coordinate.  Elements outside any mjx-mtable
        // get a singleton key based on their own identity.
        const groups = new Map();
        const tableIds = new WeakMap();
        let nextId = 1;
        const idOf = el => {
            if (!tableIds.has(el)) tableIds.set(el, nextId++);
            return tableIds.get(el);
        };
        const flag = 'xkcdRule' + side;
        container.querySelectorAll('*').forEach(el => {
            if (el.dataset[flag]) return;
            // Skip elements whose top border we already own: sqrt vinculums
            // (xkcdHidden) and \hline cells (xkcdRuleTop).  Left/Right passes
            // still see these — \hline cells also carry vertical borders.
            if (side === 'Top' && (el.dataset.xkcdHidden || el.dataset.xkcdRuleTop)) return;
            if (el.closest('mjx-surd') || el.closest('mjx-stretchy-v')) return;
            const cs = getComputedStyle(el);
            if (cs['border' + side + 'Style'] !== 'solid') return;
            const w = parseFloat(cs['border' + side + 'Width']);
            if (w <= 0) return;
            const r = el.getBoundingClientRect();
            const coord = side === 'Top'  ? r.top
                        : side === 'Left' ? r.left
                        :                   r.right;
            const table = (el.tagName === 'MJX-MTD') ? el.closest('mjx-mtable') : null;
            const key = table
                ? 'tbl' + idOf(table) + '|' + side + '|' + Math.round(coord)
                : 'el'  + idOf(el)    + '|' + side;
            let g = groups.get(key);
            if (!g) { g = { side, w, cells: [] }; groups.set(key, g); }
            g.cells.push(el);
        });
        return groups;
    }

    function _bboxOfCells(cells) {
        let left = Infinity, top = Infinity, right = -Infinity, bottom = -Infinity;
        for (const c of cells) {
            const r = c.getBoundingClientRect();
            if (r.left < left)     left = r.left;
            if (r.top < top)       top = r.top;
            if (r.right > right)   right = r.right;
            if (r.bottom > bottom) bottom = r.bottom;
        }
        return { left, top, right, bottom };
    }

    function _ensureRelative(container) {
        if (getComputedStyle(container).position === 'static') {
            container.dataset.xkcdPos = '1';
            container.style.position = 'relative';
        }
    }

    function replaceVinculums(root) {
        const scope = root || document;
        scope.querySelectorAll('mjx-container[jax="CHTML"]').forEach(container => {
            const groups = _collectRuleGroups(container, 'Top');
            if (!groups.size) return;
            _ensureRelative(container);
            const containerRect = container.getBoundingClientRect();
            for (const g of groups.values()) {
                const bb = _bboxOfCells(g.cells);
                for (const c of g.cells) {
                    c.style.borderTopColor = 'transparent';
                    c.dataset.xkcdRuleTop = '1';
                }
                const barH = Math.max(g.w, 1);
                const renderedH = barH * BAR_PIXEL_MULT;
                const left = bb.left - containerRect.left;
                const top  = (bb.top - containerRect.top) + (barH / 2) - (renderedH / 2);
                const width = bb.right - bb.left;
                container.insertAdjacentHTML('beforeend',
                    `<div class="xkcd-overlay" style="position:absolute;left:${left}px;top:${top}px;pointer-events:none;">${vinculumHTML(width, renderedH)}</div>`);
            }
        });
    }

    function replaceVerticalRules(root) {
        const scope = root || document;
        scope.querySelectorAll('mjx-container[jax="CHTML"]').forEach(container => {
            for (const side of ['Left', 'Right']) {
                const groups = _collectRuleGroups(container, side);
                if (!groups.size) continue;
                _ensureRelative(container);
                const containerRect = container.getBoundingClientRect();
                for (const g of groups.values()) {
                    const bb = _bboxOfCells(g.cells);
                    for (const c of g.cells) {
                        c.style['border' + side + 'Color'] = 'transparent';
                        c.dataset['xkcdRule' + side] = '1';
                        c.dataset['xkcdVrule' + side] = '1';  // legacy flag for resetOverlays
                    }
                    const barW = Math.max(g.w, 1);
                    const renderedW = barW * BAR_PIXEL_MULT;
                    const lineX = (side === 'Left' ? bb.left : bb.right) - containerRect.left;
                    const x = lineX - renderedW / 2;
                    const y = bb.top - containerRect.top;
                    const lineH = bb.bottom - bb.top;
                    // CSS rotate(90deg) around the overlay's top-left corner
                    // pivots a horizontal-emdash SVG of (length=lineH,
                    // thickness=renderedW) into a vertical line.  After the
                    // rotation, content originally x∈[0,h], y∈[0,w] lands at
                    // page x∈[anchorX-w, anchorX], y∈[anchorY, anchorY+h], so
                    // we anchor at (x + renderedW, y).
                    container.insertAdjacentHTML('beforeend',
                        `<div class="xkcd-overlay" style="position:absolute;left:${x + renderedW}px;top:${y}px;transform:rotate(90deg);transform-origin:0 0;pointer-events:none;">${vinculumHTML(lineH, renderedW)}</div>`);
                }
            }
        });
    }

    // ── Stretchy braces (\begin{cases}, \left\{ ... \right\}) ──────────────
    // MathJax CHTML stacks U+23A7/8/9/A glyphs in <mjx-stretchy-v> to build a
    // tall { — xkcd-script doesn't ship those codepoints so the result is the
    // mismatched-pieces salad we want to avoid.  We replace the assembly with
    // a single hand-drawn brace, stretched on the Y axis by the same cut-and-
    // extend renderer used for the surd.
    //
    // We always render the hand-drawn tall variant; MathJax only ever
    // invokes us for braces that need stretching (2+ rows), and at any of
    // those heights the .tall artwork stretches more gracefully than the
    // basic letter-size {.
    const BRACE_GLYPH = 'braceleft.tall';
    // Visible brace width as a fraction of fontSize.  Anchoring Sx to the
    // glyph's natural width (rather than MathJax's MJXTEX-derived reserved
    // metrics) keeps stroke thickness consistent regardless of stretch.
    const BRACE_WIDTH_EM = 0.40;
    // How much further the rendered brace reaches than MathJax's reserved
    // height — fraction of fontSize added below.  The arm tips need a bit
    // of overrun so they visually enclose the cases content rather than
    // ending flush with the topmost / bottommost row.
    const BRACE_EXTRA_EM = 0.45;

    function replaceStretchyBraces(root) {
        const scope = root || document;
        // MathJax CHTML draws a left brace in one of two ways depending on
        // requested height:
        //   • short (2–3 row cases) — single <mjx-c class="mjx-c7B TEX-Sn">
        //     inside an <mjx-mo>, with MathJax picking size-n (TEX-S1..S4).
        //     MJXTEX's TEX-Sn variants are engineered to be tall; xkcd-
        //     script's basic { has letter metrics, so it stays small even
        //     when scaled — hence the visible undersizing.
        //   • tall (4+ row cases, \left\{...\right\} when very tall) —
        //     <mjx-stretchy-v class="mjx-c7B"> assembly with pieces stacked.
        // Both get replaced the same way: measure the visible source rect,
        // build a stretched overlay, hide the original.
        const targets = [];
        scope.querySelectorAll('mjx-stretchy-v.mjx-c7B').forEach(stretchy => {
            const mo = stretchy.closest('mjx-mo') || stretchy.parentElement;
            if (mo) targets.push({ mo, hide: stretchy, sizeFrom: stretchy });
        });
        scope.querySelectorAll('mjx-c.mjx-c7B').forEach(c => {
            // Skip TEX-S1 — that's MathJax's \big size (e.g. \bigl[...\bigr]),
            // which the user explicitly sized; overlaying a tall variant would
            // ignore that intent.  Catch S2-S4 (\Big, \bigg, \Bigg, \left...
            // \right at moderate-to-tall heights).
            const isSized = [...c.classList].some(cls => /^TEX-S[2-4]$/.test(cls));
            if (!isSized) return;  // skip inline letter-size {
            const mo = c.closest('mjx-mo');
            if (mo) targets.push({ mo, hide: c, sizeFrom: mo });
        });

        for (const { mo, hide, sizeFrom } of targets) {
            if (mo.dataset.braceReplaced) continue;
            mo.dataset.braceReplaced = '1';

            const moRect     = mo.getBoundingClientRect();
            const sourceRect = sizeFrom.getBoundingClientRect();
            if (sourceRect.height < 1 || sourceRect.width < 1) continue;

            const fontSizePx = parseFloat(getComputedStyle(mo).fontSize);
            const g = EXTENSIBLE_GLYPHS[BRACE_GLYPH];
            const natW = g.bbox.xmax - g.bbox.xmin;
            const natH = g.bbox.ymax - g.bbox.ymin;
            const Sx = (BRACE_WIDTH_EM * fontSizePx) / natW;
            const targetH_units = (sourceRect.height + BRACE_EXTRA_EM * fontSizePx) / Sx;
            const Ny = Math.max(0, targetH_units - natH);

            const segs = buildExtendedSegs(g, 0, Ny, 11);
            const d    = _segsToPath(segs);
            const eb   = _bboxOf(segs);
            const margin = 20;
            const vbX = eb.xmin - margin;
            const vbY = -(eb.ymax + margin);
            const vbW = (eb.xmax - eb.xmin) + 2 * margin;
            const vbH = (eb.ymax - eb.ymin) + 2 * margin;
            const widthPx  = vbW * Sx;
            const heightPx = vbH * Sx;

            // SVG's CSS top-left corresponds to glyph-bbox top-left minus the
            // margin in CSS pixels — back that out so the visible glyph lands
            // exactly on the source rect.
            const left = sourceRect.left - moRect.left - margin * Sx;
            const top  = sourceRect.top  - moRect.top  - margin * Sx;

            hide.dataset.xkcdHidden = '1';
            hide.style.visibility = 'hidden';
            if (getComputedStyle(mo).position === 'static') {
                mo.dataset.xkcdPos = '1';
                mo.style.position = 'relative';
            }

            const svg = `<svg class="xkcd-overlay" xmlns="http://www.w3.org/2000/svg"
                width="${widthPx}px" height="${heightPx}px" viewBox="${vbX} ${vbY} ${vbW} ${vbH}"
                style="display:block;overflow:visible;pointer-events:none;">
              <g transform="scale(1,-1)" fill="currentColor" stroke="none">
                <path d="${d}"/>
              </g>
            </svg>`;
            mo.insertAdjacentHTML('beforeend',
                `<div class="xkcd-overlay" style="position:absolute;left:${left}px;top:${top}px;pointer-events:none;">${svg}</div>`);
        }
    }

    // ── Shared sizing for stretchy ( ) and [ ] ─────────────────────────────
    // Same shape of problem as braces: MathJax CHTML either picks a sized
    // variant (mjx-c.<code> with TEX-Sn class) for moderate heights or builds
    // an mjx-stretchy-v.<code> assembly from missing tall-piece codepoints
    // for very tall cases.  Both render as letter-height glyphs inside an
    // oversized box because xkcd-script's basic ( and [ have no matching
    // stretch metrics.  We overlay a stretched outline via cut-and-extend;
    // the right side is the same overlay with a CSS scaleX(-1).
    //
    // Shared field meaning (each delim object below picks its own subset):
    //   widthEm     : visible width fraction of fontSize.  Used by bracket
    //                 (its source is near-vertical so anchoring by width is
    //                 stable; height follows from the source aspect).
    //   heightEm    : visible height fraction of fontSize at the natural
    //                 design size — i.e., the "2-row" target.  Used by paren
    //                 so the imported source artwork renders natively for
    //                 \binom without any cut-and-extend stretching; cases
    //                 smaller than 2-row uniformly shrink, taller cases
    //                 extend.
    //   extraEm     : extra reach past MathJax's reserved height — lands
    //                 below the source after the SVG y-flip + top-anchor.
    //   leftBiasEm  : rightward shift for opening delim.  Per-delim because
    //                 the curved paren needs more nudge to feel close to
    //                 content than the near-vertical bracket.
    //   rightBiasEm : rightward shift for closing delim.
    const STRETCHY_PAREN = {
        // 2-row binom target ≈ 2.5em source + 1.0em extra = 3.5em; pin
        // heightEm here so binom stays in the no-stretch branch.
        heightEm:    3.5,
        extraEm:     1.0,
        // Uniform shrink applied to Sx — both X and Y scale down by this
        // factor, then cut-and-extend stretches Y back to the required
        // target.  Net effect: strokes are proportionally thinner (and the
        // paren is correspondingly narrower) at the cost of always running
        // a small Ny pass.  Set to 1.0 to disable.
        thicknessScale: 0.65,
        leftBiasEm:  0.15,
        rightBiasEm: 0.05,
        // Paren-only compact mode (\binom).  MathJax wraps \binom parens in
        // an mjx-texatom that \left(...\right) doesn't use, so we override
        // the overhang for that context — bracket has no equivalent and
        // never reads this sub-object.
        //   extraEm         : bottom overhang below source.bottom (compact)
        //   topCropEm       : amount cropped from the top — visible top sits
        //                     this far *below* source.top so the paren feels
        //                     proportional to the short binomial stack.
        //   thicknessScale  : override the global thicknessScale; binom is
        //                     structurally smaller than fraction/matrix and
        //                     so renders at smaller Sx, making its strokes
        //                     visibly thinner.  Values >1.0 thicken strokes
        //                     at the cost of the natural glyph overshooting
        //                     `extraEm` (extra hangs further below source).
        compact: {
            extraEm:        0.3,
            topCropEm:      0.6,
            thicknessScale: 1.0,
        },
    };
    const STRETCHY_BRACKET = {
        widthEm:     0.30,
        extraEm:     1.0,
        leftBiasEm:  0.12,
        rightBiasEm: 0.05,
    };

    // ── Stretchy parentheses (\binom, \left(...\right), pmatrix) ───────────
    const PAREN_GLYPH = 'parenleft.tall';

    function replaceStretchyParens(root) {
        const scope = root || document;
        const targets = [];
        // Stretchy-v assembly (tall path)
        scope.querySelectorAll('mjx-stretchy-v.mjx-c28, mjx-stretchy-v.mjx-c29').forEach(stretchy => {
            const mo = stretchy.closest('mjx-mo') || stretchy.parentElement;
            const isRight = stretchy.classList.contains('mjx-c29');
            if (mo) targets.push({ mo, hide: stretchy, sizeFrom: stretchy, isRight });
        });
        // Sized variant (short path)
        scope.querySelectorAll('mjx-c.mjx-c28, mjx-c.mjx-c29').forEach(c => {
            // Skip TEX-S1 — that's MathJax's \big size (e.g. \bigl[...\bigr]),
            // which the user explicitly sized; overlaying a tall variant would
            // ignore that intent.  Catch S2-S4 (\Big, \bigg, \Bigg, \left...
            // \right at moderate-to-tall heights).
            const isSized = [...c.classList].some(cls => /^TEX-S[2-4]$/.test(cls));
            if (!isSized) return;
            const mo = c.closest('mjx-mo');
            const isRight = c.classList.contains('mjx-c29');
            if (mo) targets.push({ mo, hide: c, sizeFrom: mo, isRight });
        });

        for (const { mo, hide, sizeFrom, isRight } of targets) {
            if (mo.dataset.parenReplaced) continue;
            mo.dataset.parenReplaced = '1';

            const moRect     = mo.getBoundingClientRect();
            const sourceRect = sizeFrom.getBoundingClientRect();
            if (sourceRect.height < 1 || sourceRect.width < 1) continue;

            const fontSizePx = parseFloat(getComputedStyle(mo).fontSize);
            const g = EXTENSIBLE_GLYPHS[PAREN_GLYPH];
            const natW = g.bbox.xmax - g.bbox.xmin;
            const natH = g.bbox.ymax - g.bbox.ymin;
            // Height-anchored Sx with uniform thinning: Sx is shrunk by
            // thicknessScale so both X and Y of the natural glyph come in
            // smaller, then cut-and-extend stretches Y back to the target.
            // Result: proportionally thinner strokes across the whole paren
            // at the cost of a small Ny pass even on the "design-size" cases.
            const inCompact = !!mo.closest('mjx-texatom');
            const extraEm   = inCompact ? STRETCHY_PAREN.compact.extraEm
                                        : STRETCHY_PAREN.extraEm;
            const topCropEm = inCompact ? STRETCHY_PAREN.compact.topCropEm : 0;
            // Compact crops the top by `topCropEm` — shrink targetH to match
            // and (further below) shift the overlay's top down by the same
            // amount so the bottom stays anchored where it would be without
            // the crop.
            const ts = inCompact ? STRETCHY_PAREN.compact.thicknessScale
                                 : STRETCHY_PAREN.thicknessScale;
            const designedH_px = STRETCHY_PAREN.heightEm * fontSizePx;
            const targetH_px   = sourceRect.height + (extraEm - topCropEm) * fontSizePx;
            const SxFull = (targetH_px <= designedH_px)
                ? targetH_px   / natH
                : designedH_px / natH;
            const Sx = ts * SxFull;
            const Ny = Math.max(0, (targetH_px - natH * Sx) / Sx);

            const segs = buildExtendedSegs(g, 0, Ny, 13);
            const d    = _segsToPath(segs);
            const eb   = _bboxOf(segs);
            const margin = 20;
            const vbX = eb.xmin - margin;
            const vbY = -(eb.ymax + margin);
            const vbW = (eb.xmax - eb.xmin) + 2 * margin;
            const vbH = (eb.ymax - eb.ymin) + 2 * margin;
            const widthPx  = vbW * Sx;
            const heightPx = vbH * Sx;

            const biasEm = isRight ? STRETCHY_PAREN.rightBiasEm : STRETCHY_PAREN.leftBiasEm;
            const left = sourceRect.left - moRect.left - margin * Sx + biasEm * fontSizePx;
            const top  = sourceRect.top  - moRect.top  - margin * Sx + topCropEm * fontSizePx;

            hide.dataset.xkcdHidden = '1';
            hide.style.visibility = 'hidden';
            if (getComputedStyle(mo).position === 'static') {
                mo.dataset.xkcdPos = '1';
                mo.style.position = 'relative';
            }

            const svg = `<svg class="xkcd-overlay" xmlns="http://www.w3.org/2000/svg"
                width="${widthPx}px" height="${heightPx}px" viewBox="${vbX} ${vbY} ${vbW} ${vbH}"
                style="display:block;overflow:visible;pointer-events:none;">
              <g transform="scale(1,-1)" fill="currentColor" stroke="none">
                <path d="${d}"/>
              </g>
            </svg>`;
            // For the right paren, the same left-paren artwork is reflected
            // about its vertical centre via a CSS transform on the wrapper.
            const mirror = isRight ? 'transform:scaleX(-1);' : '';
            mo.insertAdjacentHTML('beforeend',
                `<div class="xkcd-overlay" style="position:absolute;left:${left}px;top:${top}px;${mirror}pointer-events:none;">${svg}</div>`);
        }
    }

    // ── Stretchy square brackets (\left[...\right], bmatrix) ──────────────
    // Same overlay machinery as parens; xkcd-script's regular `[` is straight
    // enough that we don't ship a .tall variant — the natural bracketleft
    // outline extends cleanly via a single mid-Y cut.  Right bracket is the
    // X-mirror of left, applied via CSS transform.
    const BRACKET_GLYPH = 'bracketleft';

    function replaceStretchyBrackets(root) {
        const scope = root || document;
        const targets = [];
        scope.querySelectorAll('mjx-stretchy-v.mjx-c5B, mjx-stretchy-v.mjx-c5D').forEach(stretchy => {
            const mo = stretchy.closest('mjx-mo') || stretchy.parentElement;
            const isRight = stretchy.classList.contains('mjx-c5D');
            if (mo) targets.push({ mo, hide: stretchy, sizeFrom: stretchy, isRight });
        });
        scope.querySelectorAll('mjx-c.mjx-c5B, mjx-c.mjx-c5D').forEach(c => {
            // Skip TEX-S1 — that's MathJax's \big size (e.g. \bigl[...\bigr]),
            // which the user explicitly sized; overlaying a tall variant would
            // ignore that intent.  Catch S2-S4 (\Big, \bigg, \Bigg, \left...
            // \right at moderate-to-tall heights).
            const isSized = [...c.classList].some(cls => /^TEX-S[2-4]$/.test(cls));
            if (!isSized) return;
            const mo = c.closest('mjx-mo');
            const isRight = c.classList.contains('mjx-c5D');
            if (mo) targets.push({ mo, hide: c, sizeFrom: mo, isRight });
        });

        for (const { mo, hide, sizeFrom, isRight } of targets) {
            if (mo.dataset.bracketReplaced) continue;
            mo.dataset.bracketReplaced = '1';

            const moRect     = mo.getBoundingClientRect();
            const sourceRect = sizeFrom.getBoundingClientRect();
            if (sourceRect.height < 1 || sourceRect.width < 1) continue;

            const fontSizePx = parseFloat(getComputedStyle(mo).fontSize);
            const g = EXTENSIBLE_GLYPHS[BRACKET_GLYPH];
            const natW = g.bbox.xmax - g.bbox.xmin;
            const natH = g.bbox.ymax - g.bbox.ymin;
            const Sx = (STRETCHY_BRACKET.widthEm * fontSizePx) / natW;
            const targetH_units = (sourceRect.height + STRETCHY_BRACKET.extraEm * fontSizePx) / Sx;
            const Ny = Math.max(0, targetH_units - natH);

            const segs = buildExtendedSegs(g, 0, Ny, 17);
            const d    = _segsToPath(segs);
            const eb   = _bboxOf(segs);
            const margin = 20;
            const vbX = eb.xmin - margin;
            const vbY = -(eb.ymax + margin);
            const vbW = (eb.xmax - eb.xmin) + 2 * margin;
            const vbH = (eb.ymax - eb.ymin) + 2 * margin;
            const widthPx  = vbW * Sx;
            const heightPx = vbH * Sx;

            const biasEm = isRight ? STRETCHY_BRACKET.rightBiasEm : STRETCHY_BRACKET.leftBiasEm;
            const left = sourceRect.left - moRect.left - margin * Sx + biasEm * fontSizePx;
            const top  = sourceRect.top  - moRect.top  - margin * Sx;

            hide.dataset.xkcdHidden = '1';
            hide.style.visibility = 'hidden';
            if (getComputedStyle(mo).position === 'static') {
                mo.dataset.xkcdPos = '1';
                mo.style.position = 'relative';
            }

            const svg = `<svg class="xkcd-overlay" xmlns="http://www.w3.org/2000/svg"
                width="${widthPx}px" height="${heightPx}px" viewBox="${vbX} ${vbY} ${vbW} ${vbH}"
                style="display:block;overflow:visible;pointer-events:none;">
              <g transform="scale(1,-1)" fill="currentColor" stroke="none">
                <path d="${d}"/>
              </g>
            </svg>`;
            const mirror = isRight ? 'transform:scaleX(-1);' : '';
            mo.insertAdjacentHTML('beforeend',
                `<div class="xkcd-overlay" style="position:absolute;left:${left}px;top:${top}px;${mirror}pointer-events:none;">${svg}</div>`);
        }
    }

    // ── Stretchy arrows (\xrightarrow, \overrightarrow, AMScd, etc.) ───────
    // MathJax renders horizontal stretchy arrows over/under bases via
    // <mjx-stretchy-h class="mjx-c<cp>"> (when wide) or a single <mjx-c>
    // (when narrow) inside an mjx-mo.  Vertical arrows (AMScd column
    // connectors) use <mjx-stretchy-v> the same way.  All four cardinal
    // directions reuse the arrowright outline; rotation/mirroring is via
    // CSS transform on the overlay div.
    const STRETCHY_ARROW = {
        glyph: 'arrowright',
        seed:  7,
        // Stroke weight as a fraction of fontSize.  Natural glyph height
        // is scaled to fontSizePx * heightEm; same value drives the
        // \xrightarrow operator and the \vec / \overrightarrow accent.
        heightEm: 0.35,
        // Vertical placement of the rendered arrow inside sourceRect, as
        // a fraction of sourceRect.height from the top:
        //   accent (inOver) — ink near the bottom, just above base letters.
        //   operator (horizontal, not CD) — slightly below centre so the
        //                                   label above has clearance.
        //   default (CD horizontal, CD vertical) — centred.
        yBiasFrac: { accent: 1.1, operator: 0.9, default: 0.5 },
        // AMScd cells read best with a much thinner stroke — light,
        // schematic — and the vertical column connectors need length and
        // centre tweaks so they reach between the row labels.
        cd: {
            heightEm:       0.22,
            vLengthExtraEm: 0.4,
            vYShiftEm:      0.2,
        },
        // Narrow \vec bases (single letters) bypass the SVG path and use
        // a text-rendered → at fontFrac×fontSize, which gives a more
        // natural xkcd pen than any scaled stretchy arrow.  Threshold:
        // use SVG when the natural arrow width fits within
        //   max(baseRect.width*widthFrac, fontSizePx*minWidthFrac).
        narrowVec: {
            fontFrac:     0.85,
            topLiftFrac:  0.55,
            widthFrac:    0.75,
            minWidthFrac: 0.30,
        },
    };
    // codepoint-class → {axis, transform}.  'h' axis stretches along
    // sourceRect.width; 'v' axis along sourceRect.height (the SVG is
    // built horizontally and rotated 90°).
    const ARROW_DIRS = {
        'mjx-c2192': { axis: 'h', transform: '' },                // →
        'mjx-c2190': { axis: 'h', transform: 'scaleX(-1)' },      // ←
        'mjx-c2193': { axis: 'v', transform: 'rotate(90deg)' },   // ↓
        'mjx-c2191': { axis: 'v', transform: 'rotate(-90deg)' },  // ↑
    };

    function _arrowDirForElement(el) {
        for (const cls of el.classList || []) {
            if (ARROW_DIRS[cls]) return { cls, ...ARROW_DIRS[cls] };
        }
        return null;
    }

    // Build the stretchy-arrow SVG given an X-extension `Nx` (in glyph
    // units) and a uniform scale `Sx` (px per unit).  Returns the SVG
    // markup string plus its rendered pixel dimensions so the caller can
    // position the overlay wrapper.
    function _buildArrowOverlaySvg(Nx, Sx) {
        const g = EXTENSIBLE_GLYPHS[STRETCHY_ARROW.glyph];
        const segs = buildExtendedSegs(g, Nx, 0, STRETCHY_ARROW.seed);
        const d    = _segsToPath(segs);
        const eb   = _bboxOf(segs);
        const margin = 20;
        const vbX = eb.xmin - margin;
        const vbY = -(eb.ymax + margin);
        const vbW = (eb.xmax - eb.xmin) + 2 * margin;
        const vbH = (eb.ymax - eb.ymin) + 2 * margin;
        const widthPx  = vbW * Sx;
        const heightPx = vbH * Sx;
        const svg = `<svg class="xkcd-overlay" xmlns="http://www.w3.org/2000/svg"
            width="${widthPx}px" height="${heightPx}px" viewBox="${vbX} ${vbY} ${vbW} ${vbH}"
            style="display:block;overflow:visible;pointer-events:none;">
          <g transform="scale(1,-1)" fill="currentColor" stroke="none">
            <path d="${d}"/>
          </g>
        </svg>`;
        return { svg, widthPx, heightPx };
    }

    function replaceStretchyArrows(root) {
        const scope = root || document;
        if (!EXTENSIBLE_GLYPHS[STRETCHY_ARROW.glyph]) return;
        const targets = [];
        scope.querySelectorAll('mjx-stretchy-h, mjx-stretchy-v').forEach(stretchy => {
            const dir = _arrowDirForElement(stretchy);
            if (!dir) return;
            const mo = stretchy.closest('mjx-mo') || stretchy.parentElement;
            if (mo) targets.push({ mo, hide: stretchy, sizeFrom: stretchy, dir });
        });
        scope.querySelectorAll('mjx-c').forEach(c => {
            const dir = _arrowDirForElement(c);
            if (!dir) return;
            if (c.closest('mjx-stretchy-h, mjx-stretchy-v')) return;
            const mo = c.closest('mjx-mo');
            if (!mo) return;
            // Distinguish stretchy-operator use (\xrightarrow{f},
            // \overrightarrow{f}, where the arrow's wrapper contains only
            // the arrow) from inline arrows that happen to land in an
            // over/under slot (`h \to 0` under \lim, where mo sits in an
            // mjx-mrow alongside `h` and `0`).  Walking outward from the
            // arrow, the first ancestor with multiple children is the
            // boundary: if it's the enclosing mjx-mover/munder/munderover,
            // the arrow filled a whole slot on its own — accept.
            let p = mo.parentElement;
            while (p && p.children.length === 1) p = p.parentElement;
            if (!p) return;
            if (p.tagName !== 'MJX-MOVER' && p.tagName !== 'MJX-MUNDER' &&
                p.tagName !== 'MJX-MUNDEROVER') return;
            targets.push({ mo, hide: c, sizeFrom: mo, dir });
        });

        for (const { mo, hide, sizeFrom, dir } of targets) {
            if (mo.dataset.arrowReplaced) continue;
            mo.dataset.arrowReplaced = '1';

            const moRect     = mo.getBoundingClientRect();
            const sourceRect = sizeFrom.getBoundingClientRect();
            // Stretch dimension = source rect's long axis; for vertical
            // arrows that's height (the SVG runs horizontally before the
            // CSS rotate(90deg) lays it onto the column).
            const baseStretchPx = dir.axis === 'h' ? sourceRect.width : sourceRect.height;
            if (baseStretchPx < 1) continue;

            const fontSizePx = parseFloat(getComputedStyle(mo).fontSize);
            const g    = EXTENSIBLE_GLYPHS[STRETCHY_ARROW.glyph];
            const natW = g.bbox.xmax - g.bbox.xmin;
            const natH = g.bbox.ymax - g.bbox.ymin;
            const inCD   = !!mo.closest('mjx-mtd');
            // mo sits in mjx-over for accents (\overrightarrow, \overleftarrow);
            // in mjx-base for operators with labels (\xrightarrow, \xleftarrow).
            const inOver = !!mo.closest('mjx-over, mjx-under');

            // Length extension (CD-vertical only): MathJax sizes the source
            // rect with substantial breathing room — pad so the tips reach
            // closer to both row labels.
            const stretchExtra = (inCD && dir.axis === 'v')
                ? fontSizePx * STRETCHY_ARROW.cd.vLengthExtraEm : 0;
            const stretchPx = baseStretchPx + stretchExtra;
            const heightEm = inCD ? STRETCHY_ARROW.cd.heightEm
                                  : STRETCHY_ARROW.heightEm;
            const Sx = (fontSizePx * heightEm) / natH;
            const Nx = Math.max(0, stretchPx / Sx - natW);

            const { svg, widthPx, heightPx } = _buildArrowOverlaySvg(Nx, Sx);

            // Centre horizontally; bias vertically by context (see
            // STRETCHY_ARROW.yBiasFrac docs).  CD-vertical additionally
            // shifts the centre down so it favours the lower row.
            const yBiasFrac = inOver                          ? STRETCHY_ARROW.yBiasFrac.accent
                            : (dir.axis === 'h' && !inCD)   ? STRETCHY_ARROW.yBiasFrac.operator
                                                            : STRETCHY_ARROW.yBiasFrac.default;
            const yShift = (inCD && dir.axis === 'v')
                ? fontSizePx * STRETCHY_ARROW.cd.vYShiftEm : 0;
            const cx = sourceRect.left + sourceRect.width  / 2 - moRect.left;
            const cy = sourceRect.top  + sourceRect.height * yBiasFrac
                     - moRect.top + yShift;
            const left = cx - widthPx  / 2;
            const top  = cy - heightPx / 2;

            hide.dataset.xkcdHidden = '1';
            hide.style.visibility = 'hidden';
            if (getComputedStyle(mo).position === 'static') {
                mo.dataset.xkcdPos = '1';
                mo.style.position = 'relative';
            }

            const tr = dir.transform;
            mo.insertAdjacentHTML('beforeend',
                `<div class="xkcd-overlay" style="position:absolute;left:${left}px;top:${top}px;${tr ? `transform:${tr};` : ''}pointer-events:none;">${svg}</div>`);
        }
    }

